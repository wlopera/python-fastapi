### Type Hints ###
### Es de tipado dinamico: al asignar valor el entiende que tipo es y se puede modificar en tiempo de ejecucion

my_string_variable: str= "My string variable"
print(my_string_variable)
print(type(my_string_variable))

my_string_variable = 5
print(my_string_variable)
print(type(my_string_variable))

my_typed_variable = "My typed String variable"
